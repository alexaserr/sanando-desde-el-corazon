/** Strip diacritics for fuzzy matching. */
function stripAccents(s: string): string {
  return s.normalize('NFD').replace(/[\u0300-\u036f]/g, '');
}

/**
 * Returns the full URL to the diccionario page for the given organ,
 * or undefined if no matching page exists.
 */
export function getOrganDictUrl(organName: string): string | undefined {
  const key = organName.toLowerCase().trim();
  const slug = ORGAN_SLUGS[key] ?? ORGAN_SLUGS[stripAccents(key)];
  return slug ? `${DICT_BASE}${slug}` : undefined;
}

